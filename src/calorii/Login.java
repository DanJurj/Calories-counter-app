    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        SqlConnect c=new SqlConnect();
        String username=jTextField1.getText();
        String pass=jPasswordField1.getText();
        String db_pass="";
        int id=0;
        try
        {
            Statement con1=c.con.createStatement();
            String query="Select * from Users Where Username='"+username+"'";
            ResultSet rs=con1.executeQuery(query);
            while(rs.next())
            {
                db_pass=rs.getString("Password");
                id=Integer.parseInt(rs.getString("ID"));
            }
            if(db_pass.equals(pass))
            {
                Panel c2=new Panel(id);
                c2.setVisible(true);
                c2.setSize(500,550);
                this.dispose();
            }
            else
            {
                   JOptionPane.showMessageDialog(this, "Wrong Username/Password Please Enter Again");
                   
            }
        }
        catch(Exception e)
        {
            System.out.println("Found Error : "+e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

