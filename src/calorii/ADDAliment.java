    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        SqlConnect c=new SqlConnect();
        String name=jTextField2.getText();
        String description=jTextArea2.getText();
        String categoryName=jTextField1.getText();
        int calories=Integer.parseInt(jTextField3.getText());
        int categoryID=0;
        try
        {
            Statement str=c.con.createStatement();
            ResultSet rs=str.executeQuery("Select ID from categories Where Name='"+categoryName+"'");
            while(rs.next())
            {
                categoryID=Integer.parseInt(rs.getString("ID"));
            }
        }
        catch(Exception e)
        {
            System.out.println("The error in the code is "+e);
        }
        
        try
        {
            PreparedStatement str=c.con.prepareStatement("insert into alimente(Nume,Descriere,Valoare_Calorica_100g, ID_Categorie) values(?,?,?,?)");
            str.setString(1, name);
            str.setString(2, description);
            str.setInt(3,calories);
            str.setInt(4,categoryID);
            int b = str.executeUpdate();
            JOptionPane.showMessageDialog(this, "Your Aliment has been Added Succesfully!:");
        }
        catch(Exception e)
        {
            System.out.println("Exception "+e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

